    private void update_Sellers()
    {
        TableModel table = Table_sellers.getModel();
        String id , names , fln  , sln , cellphone ,phone;
        
        id = String.valueOf(table.getValueAt(Table_sellers.getSelectedRow() , 0));
        names = (String) table.getValueAt(Table_sellers.getSelectedRow() , 1);
        fln = (String) table.getValueAt(Table_sellers.getSelectedRow() , 2);
        sln = (String) table.getValueAt(Table_sellers.getSelectedRow() , 3);
        cellphone = (String) table.getValueAt(Table_sellers.getSelectedRow() , 4);
        phone = (String) table.getValueAt(Table_sellers.getSelectedRow() , 5);
        
        d_seller.connect_DB();
        d_seller.update_seller(id , names , fln , sln , cellphone , phone);
        d_seller.investigate_sellers(Table_sellers);
        set_TableIDs_OnArray(table);//---------------------------------
        d_seller.disconnect_DB();
    }
    
    private void eliminate_Sellers(String ID)
    {
        short op;
        
        if(ID != null)
        {
            if(ID.isEmpty())
               JOptionPane.showMessageDialog(null,"seriously bro I wont find nobody without the ID","ERROR",JOptionPane.ERROR_MESSAGE);
            else
            {
                d_seller.connect_DB();
                d_seller.investigate_seller_ID(ID);
                
                   op = (short) JOptionPane.showConfirmDialog(null,"Are you sure you want to eliminate this seller ?","CONFIRMATION", JOptionPane.YES_NO_OPTION);   
                   
                   if(op == 0)
                      d_seller.delete_seller(ID);
                   
                      d_seller.investigate_sellers(Table_sellers);
                      set_TableIDs_OnArray(Table_sellers.getModel());//----------------------                                             
                              
                d_seller.disconnect_DB();             
            }
        }
    }
    
