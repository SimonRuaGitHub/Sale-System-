    private void btn_search_sellerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_search_sellerActionPerformed

        String ID = JOptionPane.showInputDialog(null,"Insert the ID please");
        //RECORDAR EL JOptionPanel showInputDialog retorna en el String , null cuando se le da cancelar y vacio cuando se le da aceptar

        if(ID != null)
        {
            if(ID.isEmpty())
            JOptionPane.showMessageDialog(null,"seriously bro I wont find nobody without the ID","ERROR",JOptionPane.ERROR_MESSAGE);
            else
            {
                d_seller.connect_DB();
                d_seller.investigate_seller_ID(ID);
                d_seller.disconnect_DB();
            }

            if(d_seller.ctc == true)
               putting_Data_Sellers();
        }
    }//GEN-LAST:event_btn_search_sellerActionPerformed

    private void btn_delete_sellerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_delete_sellerActionPerformed
        String ID = JOptionPane.showInputDialog(null,"Insert the ID please");
        short op;

        if(ID != null)
        {
            if(ID.isEmpty())
               JOptionPane.showMessageDialog(null,"seriously bro I wont find nobody without the ID","ERROR",JOptionPane.ERROR_MESSAGE);
            else
            {
                d_seller.connect_DB();
                d_seller.investigate_seller_ID(ID);

                if(d_seller.ctc == true)
                {
                    putting_Data_Sellers();
                    op = (short) JOptionPane.showConfirmDialog(null,"Are you sure you want to eliminate this seller ?","CONFIRMATION", JOptionPane.YES_NO_OPTION);

                    if(op == 0)
                       d_seller.delete_seller(ID);
                    else
                       clean_Seller();
                }
                d_seller.disconnect_DB();
            }
        }
    }//GEN-LAST:event_btn_delete_sellerActionPerformed

