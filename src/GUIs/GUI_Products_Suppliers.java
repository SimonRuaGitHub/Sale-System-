    private void eliminate_supplier(String ID) 
    {
        short op;
        
        if(ID != null)
        {
            if(ID.isEmpty())
               JOptionPane.showMessageDialog(null,"seriously bro I wont find nobody without the ID","ERROR",JOptionPane.ERROR_MESSAGE);
            else
            {
                d_supplier.connect_DB();
                d_supplier.investigate_supplier_ID(ID);
                
                op = (short) JOptionPane.showConfirmDialog(null,"Are you sure you want to eliminate this supplier ?","CONFIRMATION", JOptionPane.YES_NO_OPTION);   
                   
                   if(op == 0)
                       d_supplier.delete_supplier(ID);
                   
                d_supplier.investigate_supplier(Table_supplier);
                set_TableSupplierIDs_OnArray(Table_supplier.getModel());
                d_supplier.disconnect_DB();             
            }
        }
    }
           
