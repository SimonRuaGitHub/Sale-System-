    private String quit_spaces(String word)
    {
           String newword = "";
           short i;
           
           for(i=0; i < word.length() ; i++)
           {
               if(word.charAt(i) != ' ')
                  newword += String.valueOf(word.charAt(i));           
           }
           
           return newword;       
    }   
    
  
